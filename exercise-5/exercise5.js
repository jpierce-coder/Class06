function startToEnd(start, end) {
    let arr = [];
    for (let i = start; i <= end; i++) {
        arr.push(i);
    }
    console.log(arr);
}


startToEnd(10, 20);


/* I created the function with parameters start and end, and assigned an empty
array to the variable arr. I then looped from start incrementing through to end,
then pushing the i value into the array of arr. */
